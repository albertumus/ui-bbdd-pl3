        //Declaracion de Variables
        String user = null;
        String password = null;

        //Asignacion de valores
        user = tf_User.getText();
        password = pf_Password.getText();

        //Comprobacion de Variables Consola
//        System.out.println(user);
//        System.out.println(password);
        // F1 
        System.out.println("Probando si funciona el driver postgres");
        try {
            Class.forName("org.postgresql.Driver");
            //Class.forName("org.postgresql.Driver");
        } catch (ClassNotFoundException cnfe) {
            System.out.println("driver no disponible");
            System.out.println("error y salida");
            cnfe.printStackTrace();
            //System.exit(1);
        }
        System.out.println("Driver cargado.... empieza la conexion");

        // FASE DOS probar el manejador con su conexion (ojo cambiar usuario y password)
        

        try {
            conexion = DriverManager.getConnection("jdbc:postgresql://127.0.0.1:8000/pec3", user, password);
        } catch (SQLException se) {
            //System.out.println("No se pudo establecer la conexion");
            JOptionPane.showMessageDialog(null, se.getMessage(), "Error de Conexión", JOptionPane.ERROR_MESSAGE);
            //se.printStackTrace();
            //System.exit(1);
            tf_User.setText("");
            pf_Password.setText("");
        }
        if (conexion != null) {
            System.out.println("Conectados a la base de datos");
            try {
                System.out.println(conexion.getSchema());
            } catch (SQLException ex) {
                Logger.getLogger(Iniciar_Sesion.class.getName()).log(Level.SEVERE, null, ex);
            }
            Main_Menu vc = new Main_Menu(conexion);
            this.setVisible(false);
            vc.setVisible(true);
        } else {
            System.out.println("Problemas al conectarnos a la base de datos");
            
        }
        try {
            this.finalize();
        } catch (Throwable ex) {
            Logger.getLogger(Iniciar_Sesion.class.getName()).log(Level.SEVERE, null, ex);
        }

